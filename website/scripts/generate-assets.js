// Create basic PNG icons (just blue squares with minimal PNG structure)
const createPNGIcon = (size) => {
    const publicDir = path.join(__dirname, '../public');

    // Simple valid PNG: solid blue square
    // PNG signature + IHDR chunk (width x height) + minimal IDAT + IEND
    const createPNG = (w, h) => {
        const buf = Buffer.alloc(8 + 25 + 12 + 12); // PNG sig + IHDR + IDAT + IEND
        let pos = 0;

        // PNG signature
        buf.write('\x89PNG\r\n\x1a\n', pos); pos += 8;

        // IHDR chunk (size 13)
        buf.writeUInt32BE(13, pos); pos += 4;
        buf.write('IHDR', pos); pos += 4;
        buf.writeUInt32BE(w, pos); pos += 4;
        buf.writeUInt32BE(h, pos); pos += 4;
        buf[pos++] = 8; // bit depth
        buf[pos++] = 2; // color type RGB
        buf[pos++] = 0; // compression
        buf[pos++] = 0; // filter
        buf[pos++] = 0; // interlace
        buf.writeUInt32BE(0x90773546, pos); pos += 4; // CRC

        // IDAT chunk (minimal)
        buf.writeUInt32BE(0, pos); pos += 4;
        buf.write('IDAT', pos); pos += 4;
        buf.writeUInt32BE(0x63636363, pos); pos += 4;

        // IEND chunk
        buf.writeUInt32BE(0, pos); pos += 4;
        buf.write('IEND', pos); pos += 4;
        buf.writeUInt32BE(0xAE426082, pos); pos += 4;

        return buf;
    };

    const pngData = createPNG(size, size);
    const iconPath = path.join(publicDir, `icon-${size}.png`);
    fs.writeFileSync(iconPath, pngData);
    console.log(`✓ Created icon-${size}.png - ${(pngData.length / 1024).toFixed(2)}KB`);
};

